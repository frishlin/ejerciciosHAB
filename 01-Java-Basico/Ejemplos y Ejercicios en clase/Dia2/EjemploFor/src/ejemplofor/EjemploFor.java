
package ejemplofor;

public class EjemploFor {

    
    public static void main(String[] args) {
        
        //for(inicialización; condición de parada; modificación (incremento o decremento){}
        
        //Contar de 0 a 10 usando for
        
        for(int cont = 0; cont <= 10; cont++){
            System.out.println("Estoy en el número es: " + cont);
        }
    }
    
}
